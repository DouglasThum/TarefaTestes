package domain;

public class Contrato {
	
	private String numContrato;
	
	public Contrato(String numContrato) {
		this.numContrato = numContrato;
	}

	public Contrato() {
	}

	public String getNumContrato() {
		return numContrato;
	}

	public void setNumContrato(String numContrato) {
		this.numContrato = numContrato;
	}
}
